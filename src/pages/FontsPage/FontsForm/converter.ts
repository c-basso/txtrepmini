const fromArr = (item: string | string[]) => Array.isArray(item)
    ? randomItem(item)
    : item;

export const convert = (text: string, font: string) => {
    const conf = config[font] || {};

    return text.split('').map((letter) => {
        if (conf[letter]) {
            return fromArr(conf[letter]);
        }

        if (conf[letter.toLowerCase()]) {
            return fromArr(conf[letter.toLowerCase()]);
        }

        if (conf[letter.toUpperCase()]) {
            return fromArr(conf[letter.toUpperCase()]);
        }

        return letter;
    }).join('');
};

